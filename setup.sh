#!/bin/bash

# Create .streamlit directory
mkdir -p ~/.streamlit/

# Create config.toml for Streamlit
echo "\
[general]\n\
email = \"your-email@example.com\"\n\
" > ~/.streamlit/credentials.toml

echo "\
[server]\n\
headless = true\n\
enableCORS=false\n\
port = \$PORT\n\
" > ~/.streamlit/config.toml
